export function AdminSidebar({
  companyName = 'Grupo CT',
  userName = 'Administrador',
  userRole = 'admin',
  logoUrl,
}: AdminSidebarProps) {
  const pathname = usePathname()

  const initials = userName
    .split(' ')
    .slice(0, 2)
    .map((w) => w[0])
    .join('')
    .toUpperCase()

  return (
    <aside className="border-r border-slate-200 bg-white">
      <div className="flex h-full flex-col">

        {/* Logo y nombre del sistema */}
        <div className="flex items-center gap-3 border-b border-slate-100 px-5 py-5">
          {logoUrl ? (
            <img src={logoUrl} alt="Logo" className="h-9 w-9 rounded-xl object-contain" />
          ) : (
            <div className="flex h-9 w-9 items-center justify-center rounded-xl bg-blue-600">
              <svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="white" strokeWidth="2.5" strokeLinecap="round" strokeLinejoin="round">
                <circle cx="12" cy="12" r="10" /><polyline points="12 6 12 12 16 14" />
              </svg>
            </div>
          )}
          <div>
            <p className="text-sm font-bold text-slate-900">{companyName}</p>
            <p className="text-xs text-slate-400">Sistema de Gestión</p>
          </div>
        </div>

        {/* Navegación */}
        <div className="flex-1 overflow-y-auto px-3 py-4">
          {adminNav.map((section) => (
            <div key={section.title} className="mb-5">
              <p className="mb-1.5 px-3 text-[10px] font-bold uppercase tracking-[0.18em] text-slate-400">
                {section.title}
              </p>
              <div className="space-y-0.5">
                {section.items.map((item) => {
                  const active = isActive(pathname, item.href)
                  return (
                    <Link key={item.href} href={item.href} className={itemClass(active)}>
                      <span className={active ? 'text-blue-600' : 'text-slate-400'}>
                        {navIcons[item.href] ?? defaultIcon}
                      </span>
                      {item.label}
                    </Link>
                  )
                })}
              </div>
            </div>
          ))}
        </div>

        {/* Footer: Ir al kiosco + usuario */}
        <div className="border-t border-slate-100 px-3 py-4 space-y-2">
          {/* Botón kiosco */}
          <Link
            href="/"
            className="flex items-center gap-2.5 rounded-xl px-3 py-2.5 text-sm font-medium text-slate-500 transition hover:bg-slate-100 hover:text-slate-900"
          >
            <svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round" className="text-slate-400">
              <rect x="2" y="3" width="20" height="14" rx="2" /><line x1="8" y1="21" x2="16" y2="21" /><line x1="12" y1="17" x2="12" y2="21" />
            </svg>
            Ir al Kiosco
          </Link>

          {/* Usuario */}
          <div className="flex items-center gap-3 rounded-xl bg-slate-50 px-3 py-3">
            <div className="flex h-9 w-9 shrink-0 items-center justify-center rounded-full bg-blue-600 text-sm font-bold text-white">
              {initials}
            </div>
            <div className="min-w-0 flex-1">
              <p className="truncate text-sm font-semibold text-slate-800">{userName}</p>
              <p className="text-xs text-slate-400">{roleLabels[userRole] ?? 'Administrador'}</p>
            </div>
            <form action={signOut}>
              <button type="submit" title="Cerrar sesión" className="text-slate-400 transition hover:text-slate-700">
                <svg width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
                  <path d="M9 21H5a2 2 0 0 1-2-2V5a2 2 0 0 1 2-2h4" />
                  <polyline points="16 17 21 12 16 7" /><line x1="21" y1="12" x2="9" y2="12" />
                </svg>
              </button>
            </form>
          </div>
        </div>

      </div>
    </aside>
  )
}
